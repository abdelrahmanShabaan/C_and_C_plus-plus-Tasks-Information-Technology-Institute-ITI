#include <stdio.h>
#include <stdlib.h>

int main()
{

    /* Write c program with While Loop and make it looping until number
     be less than or equal 19 and print the results of all of them  in new line  */

    int i=0;

    while(i <= 19)
    {
        printf("%d \n" , i );
        i++;

    }




    /*Write c program with do While Loop
    and make it looping until number be less than or equal 19 and print the results of all of them  in new line  */

    //variable
    int g =0;

    do{
        printf("%d \n" , g );
        g++;

      }while(g <= 19);




    return 0;
}
