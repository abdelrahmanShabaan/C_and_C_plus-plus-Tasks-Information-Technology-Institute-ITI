#include <stdio.h>
#include <stdlib.h>

int main()
{

    /* Switch Case */

    int day = 8;


    switch(day)
    {
    case 1 :
        printf("the value is actually equal one");
        break;
    case 2:
        printf("the value is actually equal two");
        break;
    case 3:
        printf("the value is actually equal three");
        break;

    default:
        printf("we not found your actually value of variable of day");

    }

    return 0;
}
